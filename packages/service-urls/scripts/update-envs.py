"""Update environment-specific config values after cloning from local."""
import json
import urllib.request

BASE = "http://localhost:18087"

# Login
req = urllib.request.Request(
    f"{BASE}/api/v1/auth/login",
    data=json.dumps({"phoneNumber": "+1234567890", "password": "admin123"}).encode(),
    headers={"Content-Type": "application/json"},
)
token = json.loads(urllib.request.urlopen(req).read())["accessToken"]
headers = {"Authorization": f"Bearer {token}", "Content-Type": "application/json"}


def update(env, key, val):
    req = urllib.request.Request(
        f"{BASE}/api/v1/admin/service-urls/{env}/config-entries/{key}",
        data=json.dumps({"configValue": str(val)}).encode(),
        headers=headers,
        method="PUT",
    )
    try:
        urllib.request.urlopen(req)
    except Exception as e:
        print(f"  WARN: {env}/{key}: {e}")


